	for (int i = 1; i <= n; ++i) {
		cin >> a[i];
		pos[a[i]] = i;
	}
	for (int i = 1; i <= n; ++i) { 
        cin >> b[i]; 
        c[i] = pos[b[i]]; 
    }

	for (int i = 1; i <= n; ++i) {
		int p = lower_bound(begin(lis), end(lis), c[i]) - begin(lis);
		if (p == lis.size()) lis.push_back(c[i]);
		else lis[p] = c[i];
	}
