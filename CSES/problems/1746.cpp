ll LCM(int a, int b)
{
    return ((ll)a * b) / __gcd(a, b);
}

bool nCr_Parity(int n, int r){
    int i = 0;
    while(i <= 30){ // for 32 bit integers
        int x = 1<<i;
        if((x&r) > (x&n)) return 0;
        ++i;
    }
    return 1; // 1 is odd 
}

ll solve(){
    int n, m;
    cin >> n >> m;
    vvll dp(n, vll(m + 2));
    vi x(n);
    cin >> x;
    for(int i = 0; i < n; ++i){
        for(int j = 1; j <= m; ++j){
            if(i == 0 && (x[i] == 0 || j == x[i])){
                dp[i][j] = 1;
                continue;   
            }
            if(x[i] == 0 || j == x[i]){
                dp[i][j] += dp[i - 1][j - 1] + dp[i - 1][j] + dp[i - 1][j + 1];
                dp[i][j] %= MOD;
            }
        }
    }
    ll ans = 0;
    for(int j = 1; j <= m; ++j){
        ans += dp[n - 1][j];
    }
    ans %= MOD;
    return ans;
}

